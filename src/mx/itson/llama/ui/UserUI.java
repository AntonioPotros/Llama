    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try{
            int fila = tblUser.getSelectedRow();
            if(fila != -1){
                int id = (int) tblUser.getValueAt(fila,0);
                String name = (String) tblUser.getValueAt(fila,1);
                String phone = (String) tblUser.getValueAt(fila,2);
                String creditCard = (String) tblUser.getValueAt(fila,3);
                
                
                int newId = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el nuevo id",id));
                if (newId < 0) {
             return; 
                }
                String newName = JOptionPane.showInputDialog("Ingrese el nuevo nombre",name); 
                if (newName == null) {
            return;
            }
                String newPhone = JOptionPane.showInputDialog("Ingrese la nueva categoria",phone);
                if (newPhone == null) {
            return;
            }
                String newCreditCard = JOptionPane.showInputDialog("Ingrese el nuevo horario",creditCard);
                if (newCreditCard == null) {
            return;
            }
                
                User.update(newId, newName, newPhone, newCreditCard);
                
                }else{
               JOptionPane.showMessageDialog(rootPane, "Seleccione un registro");
            }
            
        }catch(Exception ex){
            System.out.println("Ocurrio un erros"+ex.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

