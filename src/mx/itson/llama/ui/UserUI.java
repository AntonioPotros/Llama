    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
           try{
            String filtro = txtSearch.getText();
            List<User> user = User.getAll(filtro);
            DefaultTableModel tableModel = (DefaultTableModel)tblUser.getModel();
            tableModel.setRowCount(0);
            
            for(User users : user){
                Object[] rowDate = {users.getId(),users.getName(),users.getPhone(),users.getCreditCard()};
                tableModel.addRow(rowDate);
            }
            
        }catch(Exception ex){
            System.out.println("Ocurrio un error"+ex.getMessage());
        }
    }//GEN-LAST:event_btnShowActionPerformed

